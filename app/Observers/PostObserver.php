<?php

namespace App\Observers;

use App\Post;

class PostObserver
{
    /**
     * Handle the post "created" event.
     *
     * @param  \App\Post  $post
     * @return void
     */
    public function created(Post $post)
    {
        $postQuery = Post::where('slug', $post->slug)->count();
        if ($postQuery > 1) {
            $renameSlug = $post->slug . $post->id;
            $post = Post::where('id', $post->id)->update(['slug' => $renameSlug]);
        }
        \Cache::flush();
    }

    /**
     * Handle the post "updated" event.
     *
     * @param  \App\Post  $post
     * @return void
     */
    public function updated(Post $post)
    {
        //
    }

    public function updating(Post $post)
